create table profiles (
  id uuid references auth.users on delete cascade primary key,
  username text unique not null,
  display_name text,
  email text unique not null,
  avatar_url text,
  bio text default '',
  birth_date date,
  show_age boolean default false,
  country text,
  show_country boolean default false,
  created_at timestamptz default now()
);

alter table profiles enable row level security;

create policy "Perfiles visibles para todos los usuarios autenticados"
  on profiles for select
  to authenticated
  using (true);

create policy "Perfiles insertables solo por service_role"
  on profiles for insert
  to service_role
  with check (true);

create table audit_logs (
  id bigint generated by default as identity primary key,
  event text not null,
  user_id uuid references auth.users on delete set null,
  email text,
  ip text,
  user_agent text,
  metadata jsonb default '{}',
  timestamp timestamptz default now()
);

alter table audit_logs enable row level security;

create policy "Audit logs insertables solo por service_role"
  on audit_logs for insert
  to service_role
  with check (
    event is not null
    and (
      metadata is null
      or jsonb_typeof(metadata) = 'object'
    )
  );

create policy "Audit logs visibles solo por service_role"
  on audit_logs for select
  to service_role
  using (true);

create table friend_requests (
  id bigint generated by default as identity primary key,
  sender_id uuid references auth.users on delete cascade not null,
  receiver_id uuid references auth.users on delete cascade not null,
  status text not null default 'pending' check (status in ('pending', 'accepted', 'rejected')),
  created_at timestamptz default now(),
  updated_at timestamptz default now()
);

create index idx_friend_requests_receiver on friend_requests(receiver_id, status);
create index idx_friend_requests_sender on friend_requests(sender_id, status);

create table followers (
  follower_id uuid references auth.users on delete cascade not null,
  following_id uuid references auth.users on delete cascade not null,
  created_at timestamptz default now(),
  primary key (follower_id, following_id)
);

alter table followers enable row level security;


create policy "Perfiles actualizables solo por service_role"
  on profiles for update
  to service_role
  using (true)
  with check (true);

-- Storage bucket for profile avatars
insert into storage.buckets (id, name, public, file_size_limit, allowed_mime_types)
values ('avatars', 'avatars', true, 2097152, '{"image/png","image/jpeg","image/gif","image/webp"}')
on conflict (id) do nothing;

create policy "Avatars visibles para usuarios autenticados"
  on storage.objects for select
  to authenticated
  using (bucket_id = 'avatars');

create policy "Avatars gestionables solo por service_role"
  on storage.objects for all
  to service_role
  using (bucket_id = 'avatars')
  with check (bucket_id = 'avatars');

create table chats (
  id uuid default gen_random_uuid() primary key,
  created_at timestamptz default now(),
  updated_at timestamptz default now()
);

create table chat_participants (
  chat_id uuid references chats on delete cascade not null,
  user_id uuid references auth.users on delete cascade not null,
  primary key (chat_id, user_id)
);

create index idx_chat_participants_user on chat_participants(user_id);

create table chat_messages (
  id bigint generated by default as identity primary key,
  chat_id uuid references chats on delete cascade not null,
  sender_id uuid references auth.users on delete cascade not null,
  content text not null,
  created_at timestamptz default now()
);

create index idx_chat_messages_chat on chat_messages(chat_id, created_at);

alter table chats enable row level security;
alter table chat_participants enable row level security;
alter table chat_messages enable row level security;

create policy "Chats gestionables solo por service_role"
  on chats for all
  to service_role
  using (true)
  with check (true);

create policy "Chat participants gestionables solo por service_role"
  on chat_participants for all
  to service_role
  using (true)
  with check (true);

create policy "Chat messages gestionables solo por service_role"
  on chat_messages for all
  to service_role
  using (true)
